#include<stdio.h>
void main(){
	int num1=2;
	int num2=100;

	int add=num1+num2;
	int sub=num1-num2;
	int mult=num1*num2;
	int div=num1/num2;

	printf("Addition of numbers is: %d \n",add);
	printf("Subtraction of numbers is: %d \n",sub);
	printf("Multiplication of numbers is: %d \n",mult);
	printf("Division of numbers is: %d \n",div);
}
